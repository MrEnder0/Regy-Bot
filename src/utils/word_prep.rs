pub fn filter_characters(input: &str) -> String {
    let output = input.to_string()
        .replace(&['а', 'ạ', 'ą', 'ä', 'à', 'á'][..], "a")
        .replace(&['ß', 'ⓑ', '⒝', 'ദ', '൫', '♭', 'ḃ', 'ḅ', 'ḇ', 'ℬ', 'Ḃ', 'Ḅ', 'Ḇ'][..], "b")
        .replace(&['с', 'ç', 'ć', 'č'][..], "c")
        .replace(&['ԁ', 'đ', 'ɗ'][..], "d")
        .replace(&['е', '€', 'ę', 'ë', 'è', 'é', 'ẹ', 'ẽ', 'ẻ'][..], "e")
        .replace(&['ƒ', 'ⓕ', '⒡', 'ḟ', 'ƒ', 'ℱ', 'Ḟ', '₣', '℉'][..], "f")
        .replace(&['ğ', 'ġ', 'ģ'][..], "g")
        .replace(&['һ', 'ħ'][..], "h")
        .replace(&['ӏ', 'і', 'ï', 'ı', 'ì', 'í', 'ị', 'į', 'ĩ', 'ỉ'][..], "i")
        .replace(&['ј', 'ĵ', 'ʝ', 'ⓙ', '⒥', 'ʝ', '♩', 'Ⓙ'][..], "j")
        .replace(&['ķ', 'κ'][..], "k")
        .replace(&['ł', 'ḷ', 'ⓛ', '⒧', 'ℓ', 'ḻ', 'ḽ', 'ℒ', 'Ḷ', 'Ḹ', 'Ḻ', 'Ḽ'][..], "l")
        .replace(&['ṃ', 'ṁ'][..], "m")
        .replace(&['ո', 'ñ', 'ń', 'ņ', 'ň', 'ŉ'][..], "n")
        .replace(&['о', 'ο', 'օ', 'ö', 'ø', 'õ', 'ò', 'ó'][..], "o")
        .replace(&['р', 'ṗ'][..], "p")
        .replace(&['զ'][..], "q")
        .replace(&['ř', 'ŕ', 'ŗ'][..], "r")
        .replace(&['ʂ', 'š', 'ş', 'ś', 'ṣ', 'ṡ'][..], "s")
        .replace(&['ţ', 'ť', 'ṭ', 'ṫ'][..], "t")
        .replace(&['ս', 'υ', 'ü', 'ù', 'ú', 'ụ', 'ų', 'ū', 'ũ', 'ủ', 'ư'][..], "u")
        .replace(&['ν', 'ѵ', 'ṿ'][..], "v")
        .replace(&['ẁ', 'ẃ', 'ẅ', 'ẇ', 'ẉ', 'ẘ'][..], "w")
        .replace(&['х', 'ҳ', 'ẋ', 'ẍ'][..], "x")
        .replace(&['у', 'ÿ', 'ỳ', 'ý', 'ỵ', 'ỹ', 'ỷ'][..], "y")
        .replace(&['ʐ', 'ž', 'ż', 'ź'][..], "z");

    output
}
